package JAVA1.OneWeek.SelfStudy.Tuesday.FlowControl;

public class NumberOutput {
    public static void main(String args[]) {
        // for문 사용해서 1~100출력
        for (int i = 1; i <= 100; i++) {
            System.out.println(i);
        }
        //while문을 사용하여 1~100출력
        int i=1;
        System.out.println("while문");

        while(i<=100){

            System.out.println(i);
            i++;
        }



    }
    }

