package JAVA1.OneWeek.SelfStudy.Thursday.Practice.Tuesday.DataTypes;

public class VarietyOperators {
    public static void main(String args[]) {
        int a = 3;
        int b = 2;
        int c=0;
        System.out.println(a + b);
        System.out.println(a - b);

        // 곱하기
        System.out.println(a*b);

        // 나누기
        System.out.println(a/b);

        // 나머지
        System.out.println(a%b);

        // 음수
        System.out.println(-a);
    }
}
